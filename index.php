        <p>
            Buď vítán na stránkách křesťanské mládeže prostějovského děkanátu. Tady jsi opravdu správně. I
            zde jsi křesťanem. Najdeš tady mnoho užitečných informací o našich akcích a novinek ze života mladých
            lidí, se kterými sdílíš stejnou víru, tak jak to na dobrých webovkách má být. Ale co je důležitější,
            můžeš se zde setkat nejen se slovem lidským, ale i se slovem Božím, které proniká až do morku kostí
            tomu, kdo se mu otevře.
        </p>
        <p>
            Děkuji všem, kdo se podílíte na dobrém a užitečném obsahu těchto stránek. Ale také Vám, našim
            čtenářům, věrným návštěvníkům a přispěvatelům. A se všemi se těším na osobní setkání při
            některých z našich akcí.
        </p>
